    /**
     * Get city resource by state
     *
     * @param App\Http\Requests $request
     * @param int $id
     *
     * @return mixed
     */
    public function getCityByState(Request $request, $id)
    {
        if ($request->ajax()) {

            $cities = City::where('state_id', $id)->select(['id', 'name'])->get();

            if (!empty($cities)) {

                return response()->json(['success' => true, 'message' => 'Data fetched', 'data' => $cities]);

            }

        }

        return response()->json(['success' => false, 'message' => 'Something went wrong']);
    }
